# Variables in Python

a = 101         # Integer value (Any Number +ve or -ve)
b = 100.1       # Floating Point value (Number having Decimal value)
c = "Python"    # String value (set or array of Characters)
d = True        # Boolean value (True or False)
e = None        # Nonetype value (Stores None in variable)

print("-" * 20)
print(a); print(type(a))
print(b); print(type(b))
print(c); print(type(c))
print(d); print(type(d))
print(e); print(type(e))

print("-" * 20)