# Simple overview of Python's main data types
# This script provides an overview of Python's primary built-in data types,
# complete with explanations and usage examples.

# Integer
num = 10
print(num, type(num))  # 10 <class 'int'>

# Float
pi = 3.14
print(pi, type(pi))    # 3.14 <class 'float'>

# String
text = "Hello"
print(text, type(text))  # Hello <class 'str'>

# Boolean
is_valid = True
print(is_valid, type(is_valid))  # True <class 'bool'>

# NoneType
nothing = None
print(nothing, type(nothing))
